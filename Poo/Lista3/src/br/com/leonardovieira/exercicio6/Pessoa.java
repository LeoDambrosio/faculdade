package Lista3.src.br.com.leonardovieira.exercicio6;


public class Pessoa {
    private String nome;
    private int idade;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public boolean verificarIdade() {
        return idade >= 18;
    }
}

