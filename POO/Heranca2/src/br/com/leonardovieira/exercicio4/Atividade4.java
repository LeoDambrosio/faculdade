public class Atividade4 {
    //Explique com suas palavras por que não podemos ter construtores declarados com a palavra-chave abstract.

    //Resposta
    //Não podemos usar a palavra-chave abstract em um construtor porque construtores não podem ser sobrescritos (overridden) por subclasses — e isso é exatamente o que a palavra abstract exige: que algo seja obrigatoriamente implementado nas subclasses.
}
